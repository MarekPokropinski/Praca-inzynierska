package FuzzySystems.DTOs;

public class TriangleNumberDTO {
    private float a,b,c;

    public TriangleNumberDTO() {}

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

    public float getC() {
        return c;
    }

    public void setC(float c) {
        this.c = c;
    }
}
